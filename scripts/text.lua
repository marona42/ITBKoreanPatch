function IsText(id)	
	return IsLocalizedText(id)
end

function GetText(id, r1, r2, r3)

	local found = IsLocalizedText(id)
	local text = GetLocalizedText(id)
	
	if not found then
		if Global_Texts ~= nil and Global_Texts[id] ~= nil then
			text = Global_Texts[id]
			found = true
		end
		
		if Weapon_Texts ~= nil and Weapon_Texts[id] ~= nil then
			text = Weapon_Texts[id]
			found = true
		end
		
		if Achievement_Texts ~= nil and Achievement_Texts[id] ~= nil then
			text = Achievement_Texts[id]
			found = true
		end
		
		if Mission_Texts ~= nil and Mission_Texts[id] ~= nil then
			text = Mission_Texts[id]
			found = true
		end
		
		if Pawn_Texts ~= nil and Pawn_Texts[id] ~= nil then
			text = Pawn_Texts[id]
			found = true
		end
	end
		
	if r1 ~= nil and r1 ~= "" then
		text = string.gsub(text,"$1",r1)
	end
	
	if r2 ~= nil and r2 ~= "" then
		text = string.gsub(text,"$2",r2)
	end
	
	if r3 ~= nil and r3 ~= "" then
		text = string.gsub(text,"$3",r3)
	end
	
	if id ~= "" and text=="" and IsLanguageText("English", id) and GetLanguageText("English", id) ~= "" then
		text = "["..id.."]"
	end

	return text
end

function GetDialogText(id)

  text = GetText(id)
    
  if Game ~= nil then
    text = string.gsub(text, "#squad", Game:GetSquad())
    text = string.gsub(text, "#corp", GetText(Game:GetCorp().bark_name))
  end
  
  return text

end